-- 2. Напишите запрос, позволяющий вывести все строки таблицы
-- EXAM_MARKS, в которых предмет обучения имеет номер (SUBJ_ID),
-- равный 12.

select em.exam_id as "идентификатор экзамена",
       student_id as "идентификатор студента",
       subj_id    as "идентификатор предмета обучения",
       mark       as "экзаменационная оценка",
       exam_date  as "дата экзамена"
from exam_marks as em
where em.subj_id = 10;
